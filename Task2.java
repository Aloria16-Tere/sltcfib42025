public class Main {
    public static void main(String[] args) {
        // Create the primitive variables with the required values
        char h = 'H';
        int threeOneOneZero = 3110;
        String spaceWorld = " world ";
        float twoPointZero = 2.0f;
        boolean trueValue = true;

        // Concatenate and print all primitives in a single line.
        // The `+` operator automatically converts the primitive types to strings.
        System.out.println(h + threeOneOneZero + spaceWorld + twoPointZero + " " + trueValue);
    }
}
